class AttackSimulator:
    """Generate benign test inputs that exercise defense and detection logic.

    All generated scenarios use structurally representative inputs that
    exercise defensive code paths. No real attack payloads are generated.

    Parameters
    ----------
    seed_prefix:
        Prefix used when generating scenario IDs for traceability.

    Example
    -------
    ::

        simulator = AttackSimulator()
        scenarios = simulator.generate_scenarios("ASI-01")
        for s in scenarios:
            print(s.label, s.expected_detection)
    """

    def __init__(self, *, seed_prefix: str = "sim") -> None:
        self._seed_prefix = seed_prefix

    def generate_scenarios(
        self,
        category: str,
        *,
        count: int | None = None,
    ) -> list[SimulationScenario]:
        """Generate test scenarios for a given OWASP ASI category.

        Parameters
        ----------
        category:
            The ASI category to generate scenarios for (e.g. ``"ASI-01"``).
        count:
            Maximum number of scenarios to return. Returns all available
            if not specified.

        Returns
        -------
        list[SimulationScenario]
            List of test scenarios (never contains real attack payloads).

        Raises
        ------
        ValueError
            If the category is not supported.
        """
        templates = _SCENARIO_TEMPLATES.get(category.upper())
        if templates is None:
            raise ValueError(
                f"No scenario templates for category {category!r}. "
                f"Supported: {sorted(_SCENARIO_TEMPLATES.keys())}"
            )

        selected = templates[:count] if count is not None else templates
        scenarios: list[SimulationScenario] = []
        for index, template in enumerate(selected):
            scenario_id = f"{self._seed_prefix}-{category.upper()}-{index:03d}"
            scenarios.append(
                SimulationScenario(
                    scenario_id=scenario_id,
                    category=category.upper(),
                    label=template["label"],
                    input_data=dict(template["input"]),
                    expected_detection=bool(template["expected"]),
                    notes=template.get("notes", ""),
                )
            )
        return scenarios

    def list_supported_categories(self) -> list[str]:
        """Return list of OWASP ASI categories with available scenarios."""
        return sorted(_SCENARIO_TEMPLATES.keys())

    def generate_all(self) -> dict[str, list[SimulationScenario]]:
        """Generate all available scenarios for all supported categories.

        Returns
        -------
        dict[str, list[SimulationScenario]]
            Category → list of SimulationScenario.
        """
        return {
            category: self.generate_scenarios(category)
            for category in self.list_supported_categories()
        }
