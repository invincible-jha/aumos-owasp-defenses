"""AttackSimulator — generate benign test inputs that exercise defense logic.

Generates structurally representative (but non-harmful) test inputs to verify
that defense and detection logic works correctly. NO real attack payloads are
generated. All inputs are designed for testing defensive code paths only.

Example
-------
::

    from aumos_owasp_defenses.defenses_suite.simulator import AttackSimulator

    simulator = AttackSimulator()
    scenarios = simulator.generate_scenarios("ASI-01", count=5)
    for scenario in scenarios:
        print(scenario.label, scenario.expected_detection)
"""
from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class SimulationScenario:
    """A single test scenario generated by the simulator.

    Attributes
    ----------
    scenario_id:
        Unique identifier for this scenario.
    category:
        The OWASP ASI category this scenario tests.
    label:
        Human-readable description of the scenario.
    input_data:
        The test input to run through the detector/defense.
    expected_detection:
        Whether this input should be detected as a threat.
    notes:
        Additional notes about why this scenario tests a particular behavior.
    """

    scenario_id: str
    category: str
    label: str
    input_data: dict[str, object]
    expected_detection: bool
    notes: str = ""

    def to_dict(self) -> dict[str, object]:
        """Serialise to a plain dictionary."""
        return {
            "scenario_id": self.scenario_id,
            "category": self.category,
            "label": self.label,
            "input_data": self.input_data,
            "expected_detection": self.expected_detection,
            "notes": self.notes,
        }


